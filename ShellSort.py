def shell_sort(arr):
    """
    Сортировка Шелла с последовательностью n/2, n/4, ..., 1
    """
    n = len(arr)
    gap = n // 2  # Начальный шаг
    
    while gap > 0:
        # Выполняем сортировку вставками для этого шага
        for i in range(gap, n):
            temp = arr[i]
            j = i
            # Сдвигаем элементы, пока не найдем правильную позицию
            while j >= gap and arr[j - gap] > temp:
                arr[j] = arr[j - gap]
                j -= gap
            arr[j] = temp
        gap //= 2  # Уменьшаем шаг
    return arr

# Пример использования
numbers = [64, 34, 25, 12, 22, 11, 90, 5, 77, 30]
print("До сортировки:", numbers)
shell_sort(numbers)
print("После сортировки:", numbers)
# До сортировки: [64, 34, 25, 12, 22, 11, 90, 5, 77, 30]
# После сортировки: [5, 11, 12, 22, 25, 30, 34, 64, 77, 90]
